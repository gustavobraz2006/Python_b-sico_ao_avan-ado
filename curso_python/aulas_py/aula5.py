# Tipo de dado bool (boolean)
# ao questionar se um valor é verdadeiro ou falso, o Python retorna True ou False
# existem vários operadores para "questionar" se um valor é verdadeiro ou falso
# dentre eles o == , que compara se dois valores são iguais
print(1 == 1) # True
print(1 == 2) # False
print(type(1 == 1)) # True
